    private void refreshMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_refreshMouseClicked
        cetak_agama.setText(" ");
        cetak_kelamin.setText(" ");
        cetaknama.setText(" ");
        cetak_hoby.setText(" ");
        cetak_alamat.setText(" ");
        cb_agama.setSelectedItem("Pilih");
        nama.setText(" ");
        alamat.setText(" ");
        buttonGroup1.clearSelection();
        bola.setSelected(false);
        game.setSelected(false);
        programing.setSelected(false);
        Tebal.setSelected(false);
        Miring.setSelected(false);
        nama.requestFocus();
    }//GEN-LAST:event_refreshMouseClicked

