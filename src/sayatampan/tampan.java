  void kelamin(){
    String jk;
          if(Pria.isSelected()){
          jk = "Pria";
          }else if(Wanita.isSelected()){
          jk = "Wanita";
          }else{
          jk = " ";
          }
          cetak_kelamin.setText(jk);
  }
  void hoby(){
  String pbola = "";
  if(bola.isSelected()){
  pbola = "Main Bola";
  }
  String ppGame = " ";
  if(game.isSelected()){
  ppGame = "Main Game";
  }
  String reming = " ";
  if(programing.isSelected()){
   reming = "Programming";
  }
  cetak_hoby.setText(pbola+" "+ppGame+""+reming);
  }
  void temi(){
  int miring = 0;
  if (Miring.isSelected()){
  miring = Font.ITALIC;
  }
  int tebal = 0;
  if (Tebal.isSelected()){
  tebal = Font.BOLD;
  }
  cetaknama.setFont (new Font("Arial",miring+tebal,18));
  cetaknama.setText (nama.getText());
  cetak_alamat.setFont (new Font("Arial",miring+tebal,18));
  cetak_alamat.setText (alamat.getText());
  cetak_agama.setFont (new Font("Arial",miring+tebal,18));
  String a = cb_agama.getSelectedItem().toString();
  cetak_agama.setText(a);
  cetak_hoby.setFont (new Font("Arial",miring+tebal,18));
  hoby();
  cetak_kelamin.setFont (new Font("Arial",miring+tebal,18));
  kelamin();
  }
